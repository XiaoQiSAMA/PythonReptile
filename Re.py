import re
"""
Re库的主要函数功能
re.search()             搜索匹配正则表达式的第一个位置，返回match对象
re.match()              从开始位置起匹配正则表达式，返回match对象
re.findall()            搜索字符串。以列表类型返回匹配子串
re.spilt()              将字符串按照正则表达式匹配结果分割。，返回列表类型
re.finditer()           搜索字符串，返回匹配的迭代类型，每个都是match对象
re.sub()                替换所有匹配正则表达式的子串，返回替换后的字符串
"""